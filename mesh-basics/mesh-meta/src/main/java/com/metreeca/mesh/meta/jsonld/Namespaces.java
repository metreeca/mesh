package com.metreeca.mesh.meta.jsonld;

import java.lang.annotation.Retention;
import java.lang.annotation.Target;

import static java.lang.annotation.ElementType.TYPE;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

/**
 * Container annotation for multiple {@link Namespace} annotations.
 *
 * <p>This is a container annotation that holds multiple {@link Namespace} annotations when
 * applied to a single {@link Frame} interface. It is automatically generated by the Java compiler when multiple
 * {@code @Namespace} annotations are applied to a single element.</p>
 *
 * @see Namespace
 */
@Target(TYPE)
@Retention(RUNTIME)
public @interface Namespaces {

    /**
     * Retrieves the array of namespace annotations.
     *
     * @return the array of contained {@link Namespace} annotations
     */
    Namespace[] value();

}
